    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int userList = this.txtTableUsers.getModel().getRowCount();
        UserDao dao = new UserDao();

        //Consulta cada una de las filas de la tabla para saber que filas estan seleccionadas
        for (int i = 0; i < userList; i++) {
            boolean isSelected = (boolean) this.txtTableUsers.getValueAt(i, 0);
            String userName = (String) this.txtTableUsers.getValueAt(i, 2);
            if (isSelected) {
                //obtiene el id del usuario a eliminar                
                String idLogin = (String) this.txtTableUsers.getValueAt(i, 1);
                if (dao.deleteUser(idLogin)) {
                    JOptionPane.showMessageDialog(this, "User " + userName + " deleted successfully");
                } else {
                    JOptionPane.showMessageDialog(this, "Error deleting " + userName + " user");
                }
            }
        }
        this.llenarTablaUsuarios();
    }//GEN-LAST:event_jButton3ActionPerformed

