    private void llenarTablaUsuarios() {
        try {
            Connection con = ConnectionBD.getConnection();
            Statement stmt = con.createStatement();
            String query = "SELECT id_Login, Nombre, Apellido, Documento FROM usuarios";
            ResultSet rs = stmt.executeQuery(query);

            DefaultTableModel model = new DefaultTableModel() {
                Class[] types = new Class[]{
                    java.lang.Boolean.class, java.lang.Object.class, java.lang.Object.class, java.lang.Object.class, java.lang.Object.class
                };

                @Override
                public Class getColumnClass(int columnIndex) {
                    return types[columnIndex];
                }
            };

            model.addColumn("Sel");
            model.addColumn("idLogin");
            model.addColumn("Nombre");
            model.addColumn("Apellido");
            model.addColumn("Documento");

            if (txtTableUsers.getRowCount() > 0) {
                model.setRowCount(0);
            }

            while (rs.next()) {
                String idLogin = rs.getString("id_Login");
                String nombre = rs.getString("Nombre");
                String apellido = rs.getString("Apellido");
                String documento = rs.getString("Documento");
                model.addRow(new Object[]{false, idLogin, nombre, apellido, documento});
            }

            txtTableUsers.setModel(model);

            // Add an ItemListener to the checkboxes in the table
            TableColumnModel columnModel = txtTableUsers.getColumnModel();
            TableColumn column = columnModel.getColumn(0);
            column.setCellEditor(txtTableUsers.getDefaultEditor(Boolean.class));
            column.setCellRenderer(txtTableUsers.getDefaultRenderer(Boolean.class));
            JCheckBox checkBox = new JCheckBox();
            checkBox.addItemListener(new ItemListener() {
                public void itemStateChanged(ItemEvent e) {
                    int row = txtTableUsers.getSelectedRow();

                    int userList = txtTableUsers.getModel().getRowCount();
                    //deselecciona cada una de los checkbox en la tabla
                    for (int i = 0; i < userList; i++) {
                        if (i != row) {
                            txtTableUsers.setValueAt(false, i, 0);
                        }
                    }
                }
            });
            column.setCellEditor(new DefaultCellEditor(checkBox));

            rs.close();
            stmt.close();
            //con.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

